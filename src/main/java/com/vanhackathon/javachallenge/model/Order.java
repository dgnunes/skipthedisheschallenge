package com.vanhackathon.javachallenge.model;

import java.util.Date;
import java.util.List;

/**
 *
 * @author douglas
 */
public class Order {
    private int orderId;
    private Date orderDate;
    private int customerId;
    private int storeId;
    private String orderAddress;
    private String orderContact;
    
    private String orderStatus;
    private Date orderLastUpdate;
    
    private List<OrderItem> orderItems;

    public Order() {
    }

    public int getOrderId() {
        return orderId;
    }

    public void setOrderId(int orderId) {
        this.orderId = orderId;
    }

    public Date getOrderDate() {
        return orderDate;
    }

    public void setOrderDate(Date orderDate) {
        this.orderDate = orderDate;
    }

    public int getCustomerId() {
        return customerId;
    }

    public void setCustomerId(int customerId) {
        this.customerId = customerId;
    }

    public int getStoreId() {
        return storeId;
    }

    public void setStoreId(int storeId) {
        this.storeId = storeId;
    }

    public String getOrderAddress() {
        return orderAddress;
    }

    public void setOrderAddress(String orderAddress) {
        this.orderAddress = orderAddress;
    }

    public String getOrderContact() {
        return orderContact;
    }

    public void setOrderContact(String orderContact) {
        this.orderContact = orderContact;
    }

    public String getOrderStatus() {
        return orderStatus;
    }

    public void setOrderStatus(String orderStatus) {
        this.orderStatus = orderStatus;
    }

    public Date getOrderLastUpdate() {
        return orderLastUpdate;
    }

    public void setOrderLastUpdate(Date orderLastUpdate) {
        this.orderLastUpdate = orderLastUpdate;
    }

    public List<OrderItem> getOrderItems() {
        return orderItems;
    }

    public void setOrderItems(List<OrderItem> orderItems) {
        this.orderItems = orderItems;
    }

    
    
}
